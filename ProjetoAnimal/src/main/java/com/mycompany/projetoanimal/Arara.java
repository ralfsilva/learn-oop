package com.mycompany.projetoanimal;

public class Arara extends Ave {

    private String tipoArara;
    private String tamanho;
    private String tempoDeVida;

    public String getTipoArara() {
        return tipoArara;
    }

    public void setTipoArara(String tipoArara) {
        this.tipoArara = tipoArara;
    }

    public String getTamanho() {
        return tamanho;
    }

    public void setTamanho(String tamanho) {
        this.tamanho = tamanho;
    }

    public String getTempoDeVida() {
        return tempoDeVida;
    }

    public void setTempoDeVida(String tempoDeVida) {
        this.tempoDeVida = tempoDeVida;
    }
    
    
    
}
